#include <stdio.h>                //เพิ่ม # หน้า include และเพิ่ม <> ทั้งหน้าและหลังของ stdio.h

int main() {
    int a = 10;                   // กำหนด a=10 และเพิ่ม ; ท้ายประโยค
    printf("a = %d" , a);         // แสดงตัวแปร a และเพิ่ม ; ท้ายประโยค

    return 0;                     // จบการทำงาน และเพิ่ม ; ท้ายประโยค
}